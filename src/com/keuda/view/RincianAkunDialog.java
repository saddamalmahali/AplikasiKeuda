    public void initDataComboDipa(int tahun){
        
        BusinessLogic logic = new BusinessLogic(k_mainForm.k_conn);
        cboNodipModel.removeAllElements();
        
        if(tahun != -1 || tahun > 0){
            Dipa list = null;
            try {
                list = logic.getAllDipaByTahunAnggaran(tahun, k_mainForm.k_sessionId, IDBCConstant.MODUL_CONFIGURATION);
            } catch (KeudaException ex) {
                Logger.getLogger(RincianAkunDialog.class.getName()).log(Level.SEVERE, null, ex);
            }
            cboNodipModel.addElement("");
                list.setView(Dipa.VIEW_NOMOR_DIPA);
                cboNodipModel.addElement(list);
        }else{
            cboNodipModel.addElement("");
        }
    }
    
