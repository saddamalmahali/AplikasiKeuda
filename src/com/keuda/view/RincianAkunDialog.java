    public void collectData(){
        if(cboTahun.getSelectedItem().toString().trim().equals("")){
            JOptionPane.showMessageDialog(this, "Tahun Belum Dipilih", "Perhatian", JOptionPane.ERROR_MESSAGE);
        }else if(cboNodip.getSelectedItem() == null){
            JOptionPane.showMessageDialog(this, "Nomor DIPA Belum Dipilih.", "Perhatian", JOptionPane.ERROR_MESSAGE);
        }else if(txt_akun.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Akun Belum Dipilih", "Perhatian", JOptionPane.ERROR_MESSAGE);
        }else{
            k_rincian = new Rincian(k_subkomponen, k_dipa);
            k_rincian.setKoderekening(markmap.getKdrekening());
            k_rincian.setNamarekening(markmap.getNmrekening());
            
        }
    }
    
