    private void cboNodipItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboNodipItemStateChanged
        if(cboNodip.getSelectedItem() != null){
            Object obj = cboNodip.getSelectedItem();
            if(obj instanceof Dipa){
                k_dipa = (Dipa) obj;
                btnbrowse.setEnabled(true);
            }else{
                k_dipa = null;
                btnbrowse.setEnabled(false);
            }
        }
    }//GEN-LAST:event_cboNodipItemStateChanged

    private void cboTahunItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboTahunItemStateChanged
        if(!cboTahun.getSelectedItem().toString().equals("")){
            k_tahun = Integer.valueOf(cboTahun.getSelectedItem().toString().trim());
            initDataComboDipa(k_tahun);
            cboNodip.setEnabled(true);
        }else{
            k_tahun = 0;
            initDataComboDipa(k_tahun);
            cboNodip.setEnabled(false);
        }
    }//GEN-LAST:event_cboTahunItemStateChanged

