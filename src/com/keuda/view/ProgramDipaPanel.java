    private void init() throws KeudaException {
        k_tmodel.setRowCount(0);
        k_logic = new BusinessLogic(k_conn);
        ProgramDipa[] list = k_logic.getAllProgramDipa(sessionId, IDBCConstant.MODUL_CONFIGURATION);
        if(list != null){
            for(int i=0; i<list.length; i++){
                ProgramDipa program = list[i];
                program.getProgram().setView(Program.VIEW_CODE);
                program.setView(ProgramDipa.VIEW_PROGRAM);
                k_tmodel.addRow(new Object[]{program, program.getTahunAnggaran(),program.getProgram().getProgramName()});
            }
        }
        btn_hapus.setEnabled(false);
    }

    @Override
    public void valueChanged(ListSelectionEvent e) {
        if(!e.getValueIsAdjusting()){
            int iRowIndex = ((ListSelectionModel)e.getSource()).getMinSelectionIndex();
            
            if(iRowIndex != -1){
                Object obj = tableProgramDipa.getValueAt(iRowIndex, 0);
                k_ProgramDipa = (ProgramDipa) obj;
                btn_hapus.setEnabled(true);
            }
        }
    }

    
