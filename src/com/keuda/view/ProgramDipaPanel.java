    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        int panel = -1;
        panel = JOptionPane.showConfirmDialog(btn_hapus, "Apakah anda yakin akan menghapus : \n"+k_ProgramDipa.getProgram().getProgramName(), "Perhatian", JOptionPane.OK_CANCEL_OPTION);
        
        if(panel == JOptionPane.OK_OPTION){
            k_logic = new BusinessLogic(k_conn);
            try {
                k_logic.deleteProgramDipa(k_ProgramDipa.getDipaIndex(), sessionId, IDBCConstant.MODUL_CONFIGURATION);
                init();
            } catch (KeudaException ex) {
                Logger.getLogger(ProgramDipaPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }else{
            return ;
        }
        
    }//GEN-LAST:event_btn_hapusActionPerformed

